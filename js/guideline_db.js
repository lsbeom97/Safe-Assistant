/* =============================================
   유권해석 하이브리드 검색 함수
   카테고리 필터 + 키워드 점수 기반 매칭
   ============================================= */
function searchGuidelineDatabase(query, category) {
  const q = query.toLowerCase().replace(/\s+/g, ' ').trim();
  const tokens = q.split(' ').filter(t => t.length >= 1);

  const scored = GUIDELINE_DATABASE
    .filter(item => {
      if (!category || category === 'all') return true;
      return item.category === category;
    })
    .map(item => {
      let score = 0;

      // 키워드 완전 포함 (최고 점수)
      for (const kw of item.keywords) {
        const kwLower = kw.toLowerCase();
        if (q.includes(kwLower)) {
          score += kwLower.length >= 4 ? 30 : 18;
        }
      }

      // 토큰 단위 부분 매칭
      for (const token of tokens) {
        if (token.length < 2) continue;
        for (const kw of item.keywords) {
          if (kw.toLowerCase().includes(token)) score += 8;
        }
        if (item.question.toLowerCase().includes(token)) score += 5;
        if (item.answer.toLowerCase().includes(token)) score += 3;
        if (item.category.toLowerCase().includes(token)) score += 4;
        if (item.source.toLowerCase().includes(token)) score += 2;
      }

      // 출처 기관 직접 매칭
      if (q.includes('고용노동부') && item.source.includes('고용노동부')) score += 10;
      if ((q.includes('kosha') || q.includes('공단')) && item.source.includes('KOSHA')) score += 10;

      return { item, score };
    });

  return scored
    .filter(s => s.score > 0)
    .sort((a, b) => b.score - a.score)
    .slice(0, 3)
    .map(s => s.item);
}
