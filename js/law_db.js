/* =============================================
   점수 기반 다중 매칭 검색 함수
   ============================================= */
function searchLawDatabase(query) {
  const q = query.toLowerCase().replace(/\s+/g, ' ').trim();
  const tokens = q.split(' ').filter(t => t.length >= 1);

  const scored = LAW_DATABASE.map(item => {
    let score = 0;

    // 키워드 완전 매칭 (가장 높은 점수)
    for (const kw of item.keywords) {
      const kwLower = kw.toLowerCase();
      if (q.includes(kwLower)) {
        score += kwLower.length >= 5 ? 30 : 20;
      }
    }

    // 토큰 단위 키워드 부분 매칭
    for (const token of tokens) {
      if (token.length < 2) continue;
      for (const kw of item.keywords) {
        if (kw.toLowerCase().includes(token)) score += 8;
      }
      if (item.title.toLowerCase().includes(token)) score += 6;
      if (item.content.toLowerCase().includes(token)) score += 3;
      if (item.lawShort.toLowerCase().includes(token)) score += 5;
    }

    // 법령명 직접 매칭
    if (q.includes(item.lawShort.toLowerCase())) score += 10;

    return { item, score };
  }).filter(x => x.score > 0);

  // 점수 내림차순 정렬 → 상위 3개
  scored.sort((a, b) => b.score - a.score);
  return scored.slice(0, 3).map(x => x.item);
}
